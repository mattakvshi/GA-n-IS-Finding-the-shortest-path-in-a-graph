"""Экспериментальное исследование искусственной иммунной сети."""

# Пояснение: импорт из __future__ откладывает вычисление аннотаций типов и улучшает совместимость типизации.
from __future__ import annotations

# Пояснение: csv используется для сохранения результатов экспериментов в табличный CSV-файл.
import csv
# Пояснение: statistics используется для расчёта средних значений по нескольким запускам алгоритма.
import statistics

# Пояснение: импортируем функцию эталонного решения задачи кратчайшего пути методом Дейкстры.
from .dijkstra_baseline import solve_with_dijkstra
# Пояснение: импортируем генератор связного графа и функцию сохранения графа в JSON.
from .graph_model import generate_connected_graph, save_graph_to_json
# Пояснение: импортируем основной класс искусственной иммунной сети для поиска кратчайшего пути.
from .immune_network import ImmuneNetworkShortestPath
# Пояснение: percent_deviation считает отклонение от эталона, project_root возвращает корень проекта.
from .utils import percent_deviation, project_root
# Пояснение: импортируем функции построения графиков по результатам экспериментов.
from .visualization import plot_objective_history, plot_time_vs_size


# Пояснение: функция запускает серию экспериментов на графах разной размерности.
# Пояснение: возвращается список словарей, где каждый словарь — строка итоговой таблицы результатов.
def run_experiments() -> list[dict]:
    # Пояснение: строка документации описывает назначение серии экспериментов.
    """Запустить серию экспериментов и сохранить результаты.

    Параметры подобраны так, чтобы эксперимент быстро выполнялся на обычном
    ноутбуке, но всё равно показывал рост времени при увеличении графа.
    """
    # Пояснение: определяем корневую папку проекта, чтобы сохранять файлы в правильные каталоги независимо от места запуска.
    root = project_root()
    # Пояснение: data_dir — папка для сохранения сгенерированных графов.
    data_dir = root / 'data' / 'generated'
    # Пояснение: results_dir — папка для CSV-таблицы и PNG-графиков экспериментов.
    results_dir = root / 'results'
    # Пояснение: создаём папку data/generated, если она ещё не существует.
    data_dir.mkdir(parents=True, exist_ok=True)
    # Пояснение: создаём папку results, если она ещё не существует.
    results_dir.mkdir(parents=True, exist_ok=True)

    # Пояснение: cases — набор экспериментальных случаев с разным числом вершин, рёбер, итераций и размером популяции.
    # Пояснение: чем больше граф, тем больше итераций и популяция, чтобы алгоритму было достаточно пространства для поиска.
    cases = [
        # Пояснение: первый граф маленький; нужен для быстрой проверки и нижней точки на графиках.
        {'vertices': 10, 'edges': 18, 'iterations': 45, 'population': 28},
        # Пояснение: второй граф больше; показывает поведение алгоритма на средней малой размерности.
        {'vertices': 18, 'edges': 38, 'iterations': 55, 'population': 32},
        # Пояснение: третий граф ещё крупнее; нужен для анализа роста времени.
        {'vertices': 26, 'edges': 65, 'iterations': 65, 'population': 36},
        # Пояснение: четвёртый граф самый крупный в стандартной серии экспериментов.
        {'vertices': 34, 'edges': 95, 'iterations': 75, 'population': 40},
    ]

    # Пояснение: rows будет содержать итоговые строки результатов для CSV и интерфейса.
    rows: list[dict] = []
    # Пояснение: history_for_plot сохранит историю целевой функции для одного выбранного запуска.
    history_for_plot = None
    # Пояснение: optimal_for_plot сохранит эталонную длину Дейкстры для того же графика истории.
    optimal_for_plot = None

    # Пояснение: перебираем все экспериментальные случаи; index начинается с 1 и используется для seed.
    for index, case in enumerate(cases, start=1):
        # Пояснение: получаем количество вершин текущего графа из словаря case.
        vertices = case['vertices']
        # Пояснение: получаем количество рёбер текущего графа.
        edges = case['edges']
        # Пояснение: стартовая вершина фиксирована как 0 для всех экспериментов.
        start = 0
        # Пояснение: целевая вершина фиксирована как последняя вершина графа.
        target = vertices - 1
        # Пояснение: генерируем связный случайный взвешенный граф с воспроизводимым seed.
        graph = generate_connected_graph(vertices, edges, seed=100 + index)
        # Пояснение: сохраняем сгенерированный граф в JSON, чтобы его можно было позже открыть или приложить к отчёту.
        save_graph_to_json(graph, start, target, data_dir / f'graph_{vertices}_vertices.json')
        # Пояснение: считаем точный кратчайший путь методом Дейкстры; это эталон для оценки качества ИС.
        baseline = solve_with_dijkstra(graph, start, target)

        # Пояснение: run_lengths будет хранить длины путей ИС по нескольким повторным запускам на одном графе.
        run_lengths = []
        # Пояснение: run_times будет хранить время выполнения каждого повторного запуска ИС.
        run_times = []
        # Пояснение: best_path хранит лучший путь среди повторных запусков текущего случая.
        best_path = None
        # Пояснение: best_history хранит историю целевой функции для лучшего запуска текущего случая.
        best_history = None
        # Пояснение: best_length_seen — лучшая найденная длина; начинается с бесконечности, чтобы любой реальный путь был лучше.
        best_length_seen = float('inf')

        # Пояснение: выполняем два независимых запуска ИС на одном графе, чтобы усреднить время и качество.
        for run_index in range(2):
            # Пояснение: создаём объект алгоритма искусственной иммунной сети с параметрами текущего эксперимента.
            algorithm = ImmuneNetworkShortestPath(
                # Пояснение: graph — граф, на котором ищется путь.
                graph=graph,
                # Пояснение: start — начальная вершина маршрута.
                start=start,
                # Пояснение: target — конечная вершина маршрута.
                target=target,
                # Пояснение: population_size — размер популяции антител, то есть количество одновременно поддерживаемых путей.
                population_size=case['population'],
                # Пояснение: iterations — число итераций иммунного алгоритма.
                iterations=case['iterations'],
                # Пояснение: selection_size — сколько лучших антител выбирается для клонирования; минимум 6 или четверть популяции.
                selection_size=max(6, case['population'] // 4),
                # Пояснение: clone_multiplier — базовый множитель клонов для выбранных антител.
                clone_multiplier=3,
                # Пояснение: mutation_rate — вероятность гипермутации клона.
                mutation_rate=0.48,
                # Пояснение: random_injection_rate — доля случайно добавляемых антител для поддержания разнообразия.
                random_injection_rate=0.20,
                # Пояснение: memory_size — размер иммунной памяти, то есть число лучших уникальных решений.
                memory_size=6,
                # Пояснение: seed делает конкретный запуск воспроизводимым, но разным для run_index 0 и 1.
                seed=1000 + index * 10 + run_index,
            )
            # Пояснение: запускаем иммунную сеть и получаем результат поиска пути.
            result = algorithm.run()
            # Пояснение: сохраняем длину лучшего пути этого запуска.
            run_lengths.append(result.best_length)
            # Пояснение: сохраняем время выполнения этого запуска.
            run_times.append(result.elapsed_time)
            # Пояснение: если этот запуск дал лучший путь среди повторов, запоминаем его отдельно.
            if result.best_length < best_length_seen:
                # Пояснение: обновляем лучшую увиденную длину.
                best_length_seen = result.best_length
                # Пояснение: запоминаем лучший путь.
                best_path = result.best_path
                # Пояснение: запоминаем историю улучшения целевой функции для этого лучшего запуска.
                best_history = result.history

        # Пояснение: считаем среднюю длину пути по двум запускам, чтобы результат был менее случайным.
        avg_length = statistics.mean(run_lengths)
        # Пояснение: считаем среднее время выполнения по двум запускам.
        avg_time = statistics.mean(run_times)
        # Пояснение: считаем процентное отклонение средней длины ИС от точной длины Дейкстры.
        deviation = percent_deviation(avg_length, baseline.length)
        # Пояснение: row — одна строка итоговой таблицы для текущего размера графа.
        row = {
            # Пояснение: название алгоритма, которое попадёт в CSV и таблицу интерфейса.
            'algorithm': 'Immune Network',
            # Пояснение: количество вершин текущего графа.
            'graph_vertices': vertices,
            # Пояснение: фактическое количество рёбер в графе.
            'graph_edges': graph.edge_count,
            # Пояснение: стартовая вершина.
            'start_vertex': start,
            # Пояснение: целевая вершина.
            'target_vertex': target,
            # Пояснение: средняя длина пути ИС по двум запускам, округлённая для вывода.
            'best_path_length': round(avg_length, 4),
            # Пояснение: точная длина кратчайшего пути по Дейкстре.
            'dijkstra_path_length': round(baseline.length, 4),
            # Пояснение: процент отклонения от эталона Дейкстры.
            'deviation_percent': round(deviation, 4),
            # Пояснение: среднее время выполнения ИС в секундах.
            'execution_time_sec': round(avg_time, 6),
            # Пояснение: количество итераций ИС в данном эксперименте.
            'iterations': case['iterations'],
            # Пояснение: лучший найденный путь в текстовом виде через стрелки.
            'best_path': ' -> '.join(map(str, best_path or [])),
        }
        # Пояснение: добавляем строку текущего эксперимента в общий список результатов.
        rows.append(row)
        # Пояснение: историю для графика целевой функции берём только один раз — из первого экспериментального случая.
        if history_for_plot is None:
            # Пояснение: сохраняем историю лучшего запуска для последующего построения PNG-графика.
            history_for_plot = best_history
            # Пояснение: сохраняем эталонную длину Дейкстры для горизонтальной линии на графике.
            optimal_for_plot = baseline.length

    # Пояснение: путь к CSV-файлу, куда будет записана итоговая таблица экспериментов.
    csv_path = results_dir / 'experiment_results.csv'
    # Пояснение: открываем CSV-файл на запись в UTF-8.
    with csv_path.open('w', encoding='utf-8', newline='') as file:
        # Пояснение: создаём DictWriter, беря названия колонок из ключей первой строки rows[0].
        writer = csv.DictWriter(file, fieldnames=list(rows[0].keys()))
        # Пояснение: записываем заголовок CSV с названиями колонок.
        writer.writeheader()
        # Пояснение: записываем все строки результатов экспериментов.
        writer.writerows(rows)

    # Пояснение: строим график зависимости времени выполнения от количества вершин.
    plot_time_vs_size(rows, results_dir / 'time_vs_graph_size.png')
    # Пояснение: если история целевой функции была сохранена, строим график значения целевой функции по итерациям.
    if history_for_plot:
        # Пояснение: добавляем на график также эталонную линию Дейкстры через optimal_for_plot.
        plot_objective_history(history_for_plot, results_dir / 'objective_vs_iterations.png', optimal_for_plot)
    # Пояснение: возвращаем строки результатов, чтобы GUI мог отобразить их без повторного чтения CSV.
    return rows


# Пояснение: этот блок выполняется только при прямом запуске файла как модуля/скрипта.
if __name__ == '__main__':
    # Пояснение: запускаем серию экспериментов.
    rows = run_experiments()
    # Пояснение: печатаем сообщение о завершении.
    print('Эксперименты завершены. Полученные строки:')
    # Пояснение: выводим каждую строку результата в консоль.
    for row in rows:
        # Пояснение: печатаем словарь row со всеми метриками текущего экспериментального случая.
        print(row)
