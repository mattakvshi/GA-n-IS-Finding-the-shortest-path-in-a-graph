"""Общие утилиты проекта."""

# Пояснение: импорт из __future__ откладывает вычисление аннотаций типов и делает типизацию более гибкой.
from __future__ import annotations

# Пояснение: Path используется для удобной работы с путями к файлам и папкам.
from pathlib import Path
# Пояснение: json используется для сохранения Python-словарей в JSON-файлы.
import json


# Пояснение: функция определяет корневую папку проекта.
def project_root() -> Path:
    # Пояснение: строка документации поясняет, что корнем считается директория, где находится main.py.
    """Корневая папка проекта — директория, где лежит main.py."""
    # Пояснение: __file__ — путь к текущему файлу utils.py.
    # Пояснение: resolve() превращает путь в абсолютный.
    # Пояснение: parents[1] поднимается на два уровня: из src/utils.py к корню проекта.
    return Path(__file__).resolve().parents[1]


# Пояснение: функция сохраняет словарь payload в JSON-файл.
# Пояснение: payload — данные для сохранения; path — путь к выходному файлу.
def save_json(payload: dict, path: str | Path) -> None:
    # Пояснение: приводим path к объекту Path, чтобы дальше удобно работать с родительской папкой и записью файла.
    output = Path(path)
    # Пояснение: создаём родительскую папку для выходного файла, если её ещё нет.
    # Пояснение: parents=True создаёт всю цепочку папок, exist_ok=True не считает ошибкой уже существующую папку.
    output.parent.mkdir(parents=True, exist_ok=True)
    # Пояснение: преобразуем словарь в JSON-строку и записываем её в файл в кодировке UTF-8.
    # Пояснение: ensure_ascii=False сохраняет русские буквы нормально, а indent=2 делает файл читаемым.
    output.write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding='utf-8')


# Пояснение: функция считает процентное отклонение значения value от эталона baseline.
# Пояснение: в проекте это нужно для сравнения длины пути ИС с длиной точного пути Дейкстры.
def percent_deviation(value: float, baseline: float) -> float:
    # Пояснение: если эталон равен нулю, делить на него нельзя.
    if baseline == 0:
        # Пояснение: в таком пограничном случае возвращаем 0.0, чтобы не получить ошибку деления на ноль.
        return 0.0
    # Пояснение: формула процента отклонения: насколько value больше или меньше baseline относительно baseline.
    # Пояснение: если value равен baseline, результат будет 0; если value больше, отклонение будет положительным.
    return (value - baseline) / baseline * 100.0
