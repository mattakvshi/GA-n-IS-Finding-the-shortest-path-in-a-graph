"""Модель взвешенного неориентированного графа и служебные функции."""

# Пояснение: импорт из __future__ откладывает вычисление аннотаций типов; это удобно для современных подсказок типов и совместимости.
from __future__ import annotations

# Пояснение: dataclass автоматически создаёт служебные методы класса, например __init__, __repr__ и сравнение полей.
from dataclasses import dataclass
# Пояснение: Path используется для удобной и кроссплатформенной работы с путями к файлам.
from pathlib import Path
# Пояснение: Dict, List и Tuple используются в аннотациях типов, чтобы явно показать структуру словарей, списков и кортежей.
from typing import Dict, List, Tuple
# Пояснение: heapq даёт очередь с приоритетом; она нужна для эффективной реализации алгоритма Дейкстры.
import heapq
# Пояснение: json используется для чтения и сохранения графа в JSON-файлах.
import json
# Пояснение: random используется для случайной генерации графов и случайных путей.
import random


# Пояснение: декоратор dataclass превращает класс Edge в компактную структуру данных с автоматически созданным конструктором.
# Пояснение: параметр frozen=True делает объект ребра неизменяемым после создания, чтобы u, v и weight случайно не поменялись.
@dataclass(frozen=True)
# Пояснение: класс Edge описывает одно ребро графа.
class Edge:
    # Пояснение: строка документации класса; сообщает, что объект этого класса представляет ребро графа.
    """Ребро графа."""

    # Пояснение: u — номер первой вершины ребра.
    u: int
    # Пояснение: v — номер второй вершины ребра.
    v: int
    # Пояснение: weight — численный вес ребра, то есть стоимость перехода между вершинами u и v.
    weight: float


# Пояснение: класс WeightedGraph инкапсулирует структуру взвешенного неориентированного графа и операции над ним.
class WeightedGraph:
    # Пояснение: многострочная строка документации объясняет назначение класса и способ нумерации вершин.
    """Взвешенный неориентированный граф.

    В проекте граф используется для задачи поиска кратчайшего пути.
    Вершины нумеруются от 0 до n - 1.
    """

    # Пояснение: конструктор создаёт пустой граф с заданным количеством вершин.
    # Пояснение: vertices — количество вершин графа; вершины будут иметь номера от 0 до vertices - 1.
    # Пояснение: -> None означает, что метод ничего не возвращает, а только инициализирует объект.
    def __init__(self, vertices: int) -> None:
        # Пояснение: проверка защищает от бессмысленного графа из 0 или 1 вершины, где нормальный путь между разными вершинами невозможен.
        if vertices <= 1:
            # Пояснение: если вершин слишком мало, выбрасывается исключение с понятным сообщением об ошибке.
            raise ValueError("Граф должен содержать хотя бы две вершины.")
        # Пояснение: сохраняем количество вершин в поле объекта, чтобы остальные методы знали размер графа.
        self.vertices = vertices
        # Пояснение: _adj — список смежности в виде словаря; для каждой вершины хранится словарь соседей и весов рёбер.
        # Пояснение: пример структуры: {0: {1: 7.0, 3: 2.0}, 1: {0: 7.0}}.
        self._adj: Dict[int, Dict[int, float]] = {i: {} for i in range(vertices)}

    # Пояснение: метод add_edge добавляет ребро между двумя вершинами.
    # Пояснение: u и v — номера соединяемых вершин; weight — положительный вес ребра.
    def add_edge(self, u: int, v: int, weight: float) -> None:
        # Пояснение: строка документации метода; указывает, что добавляется именно неориентированное ребро.
        """Добавить неориентированное ребро."""
        # Пояснение: проверяем, что вершина u существует в диапазоне от 0 до vertices - 1.
        self._validate_vertex(u)
        # Пояснение: проверяем, что вершина v тоже существует в графе.
        self._validate_vertex(v)
        # Пояснение: запрещаем петли, то есть ребро из вершины в саму себя.
        if u == v:
            # Пояснение: если пользователь пытается добавить петлю, прекращаем выполнение метода ошибкой.
            raise ValueError("Петли в текущей реализации не используются.")
        # Пояснение: для задачи кратчайшего пути здесь используются только положительные веса.
        if weight <= 0:
            # Пояснение: нулевой или отрицательный вес некорректен для выбранной модели и может ломать смысл Дейкстры.
            raise ValueError("Вес ребра должен быть положительным.")
        # Пояснение: добавляем направление u -> v с указанным весом.
        self._adj[u][v] = float(weight)
        # Пояснение: так как граф неориентированный, добавляем обратное направление v -> u с тем же весом.
        self._adj[v][u] = float(weight)

    # Пояснение: метод возвращает всех соседей указанной вершины.
    # Пояснение: vertex — вершина, для которой нужно получить смежные вершины.
    def neighbors(self, vertex: int) -> Dict[int, float]:
        # Пояснение: строка документации метода; описывает возвращаемый смысл.
        """Получить соседей вершины."""
        # Пояснение: сначала проверяем, что запрошенная вершина действительно существует.
        self._validate_vertex(vertex)
        # Пояснение: возвращаем словарь вида {номер_соседа: вес_ребра}.
        return self._adj[vertex]

    # Пояснение: метод формирует список всех рёбер графа.
    def edges(self) -> List[Edge]:
        # Пояснение: строка документации уточняет, что каждое неориентированное ребро возвращается один раз, без обратного дубля.
        """Список рёбер без дублей."""
        # Пояснение: result — итоговый список объектов Edge.
        result: List[Edge] = []
        # Пояснение: seen хранит уже добавленные пары вершин, чтобы не вывести ребро дважды как (u, v) и (v, u).
        seen = set()
        # Пояснение: перебираем каждую вершину u и её словарь соседей.
        for u, neighbors in self._adj.items():
            # Пояснение: перебираем каждого соседа v вершины u и вес ребра между ними.
            for v, weight in neighbors.items():
                # Пояснение: key — нормализованный ключ ребра; sorted делает (2, 5) и (5, 2) одинаковым ребром.
                key = tuple(sorted((u, v)))
                # Пояснение: если такое неориентированное ребро ещё не встречалось, добавляем его в результат.
                if key not in seen:
                    # Пояснение: помечаем ребро как уже обработанное.
                    seen.add(key)
                    # Пояснение: создаём объект Edge и добавляем его в список рёбер.
                    result.append(Edge(u, v, weight))
        # Пояснение: возвращаем полный список уникальных рёбер графа.
        return result

    # Пояснение: декоратор property позволяет обращаться к количеству рёбер как к полю graph.edge_count, а не как к методу graph.edge_count().
    @property
    # Пояснение: свойство edge_count возвращает количество уникальных рёбер в неориентированном графе.
    def edge_count(self) -> int:
        # Пояснение: вызываем edges(), получаем список рёбер и возвращаем его длину.
        return len(self.edges())

    # Пояснение: метод проверяет, существует ли ребро между u и v.
    def has_edge(self, u: int, v: int) -> bool:
        # Пояснение: проверяем корректность первой вершины.
        self._validate_vertex(u)
        # Пояснение: проверяем корректность второй вершины.
        self._validate_vertex(v)
        # Пояснение: если v есть среди соседей u, значит ребро u-v существует.
        return v in self._adj[u]

    # Пояснение: метод возвращает вес ребра между вершинами u и v.
    def edge_weight(self, u: int, v: int) -> float:
        # Пояснение: если ребра нет, нельзя вернуть вес, поэтому проверяем наличие ребра.
        if not self.has_edge(u, v):
            # Пояснение: выбрасываем ошибку, чтобы вызывающий код сразу понял, что путь или запрос некорректен.
            raise ValueError(f"Ребро ({u}, {v}) отсутствует.")
        # Пояснение: возвращаем вес из списка смежности.
        return self._adj[u][v]

    # Пояснение: метод вычисляет суммарную длину пути как сумму весов последовательных рёбер.
    # Пояснение: path — список номеров вершин, например [0, 3, 5, 8].
    def path_length(self, path: List[int]) -> float:
        # Пояснение: строка документации предупреждает, что некорректный путь приводит к ValueError.
        """Длина пути. Если путь некорректен, возбуждается ValueError."""
        # Пояснение: путь должен содержать хотя бы две вершины, иначе в нём нет ни одного перехода.
        if len(path) < 2:
            # Пояснение: ошибка сообщает, что список из одной вершины не считается полноценным маршрутом.
            raise ValueError("Путь должен содержать минимум две вершины.")
        # Пояснение: total накапливает общую стоимость маршрута.
        total = 0.0
        # Пояснение: zip(path, path[1:]) создаёт пары соседних вершин: (path[0], path[1]), (path[1], path[2]) и так далее.
        for u, v in zip(path, path[1:]):
            # Пояснение: прибавляем вес очередного ребра к общей длине пути; если ребра нет, edge_weight выбросит ошибку.
            total += self.edge_weight(u, v)
        # Пояснение: возвращаем итоговую сумму весов всех переходов.
        return total

    # Пояснение: метод проверяет, является ли список вершин допустимым путём от start до target.
    def is_valid_path(self, path: List[int], start: int, target: int) -> bool:
        # Пояснение: строка документации метода.
        """Проверить, является ли список вершин допустимым путём start -> target."""
        # Пояснение: путь недопустим, если он пустой, начинается не со start или заканчивается не target.
        if not path or path[0] != start or path[-1] != target:
            # Пояснение: возвращаем False, потому что базовое условие маршрута не выполнено.
            return False
        # Пояснение: проверяем каждую пару соседних вершин в списке пути.
        for u, v in zip(path, path[1:]):
            # Пояснение: если между очередными вершинами нет ребра, то такой маршрут физически невозможен в графе.
            if not self.has_edge(u, v):
                # Пояснение: сразу возвращаем False при первом отсутствующем ребре.
                return False
        # Пояснение: если начало, конец и все рёбра корректны, путь считается допустимым.
        return True

    # Пояснение: метод удаляет циклы из пути, чтобы маршрут не ходил по лишним повторяющимся вершинам.
    def remove_cycles(self, path: List[int]) -> List[int]:
        # Пояснение: строка документации подробно объясняет назначение метода.
        """Удалить циклы из пути.

        Если вершина встречается повторно, участок между повторениями удаляется.
        Это полезно после мутаций.
        """
        # Пояснение: index_by_vertex хранит индекс последнего сохранённого появления каждой вершины в очищенном пути.
        index_by_vertex = {}
        # Пояснение: cleaned — новый путь, в который будут добавляться вершины без циклических участков.
        cleaned: List[int] = []
        # Пояснение: последовательно рассматриваем каждую вершину исходного пути.
        for vertex in path:
            # Пояснение: если вершина уже встречалась, значит между предыдущим и текущим появлением образовался цикл.
            if vertex in index_by_vertex:
                # Пояснение: idx — позиция, где эта вершина уже была в очищенном пути.
                idx = index_by_vertex[vertex]
                # Пояснение: обрезаем путь до первого появления повторной вершины включительно, удаляя цикл.
                cleaned = cleaned[: idx + 1]
                # Пояснение: пересобираем словарь индексов, потому что часть cleaned могла быть удалена.
                index_by_vertex = {v: i for i, v in enumerate(cleaned)}
            # Пояснение: если вершина встретилась впервые, просто добавляем её в очищенный путь.
            else:
                # Пояснение: запоминаем индекс, на котором вершина будет находиться в cleaned.
                index_by_vertex[vertex] = len(cleaned)
                # Пояснение: добавляем вершину в конец очищенного маршрута.
                cleaned.append(vertex)
        # Пояснение: возвращаем маршрут без повторных циклических фрагментов.
        return cleaned

    # Пояснение: метод реализует алгоритм Дейкстры и находит точный кратчайший путь.
    def shortest_path(self, start: int, target: int) -> Tuple[List[int], float]:
        # Пояснение: строка документации метода; в проекте этот метод используется как эталон и как средство ремонта путей.
        """Эталонный кратчайший путь методом Дейкстры."""
        # Пояснение: проверяем, что стартовая вершина существует.
        self._validate_vertex(start)
        # Пояснение: проверяем, что целевая вершина существует.
        self._validate_vertex(target)
        # Пояснение: distances хранит текущую лучшую известную дистанцию от start до каждой вершины.
        # Пояснение: изначально все расстояния бесконечны, потому что пути ещё не найдены.
        distances = {v: float("inf") for v in range(self.vertices)}
        # Пояснение: previous хранит предка каждой вершины в найденном кратчайшем пути; нужен для восстановления маршрута.
        previous = {v: None for v in range(self.vertices)}
        # Пояснение: расстояние от start до самой себя равно нулю.
        distances[start] = 0.0
        # Пояснение: heap — очередь с приоритетом; в ней лежат пары (текущее расстояние, вершина).
        heap: List[Tuple[float, int]] = [(0.0, start)]
        # Пояснение: пока есть вершины-кандидаты для обработки, продолжаем алгоритм.
        while heap:
            # Пояснение: извлекаем вершину u с минимальной текущей дистанцией.
            current_distance, u = heapq.heappop(heap)
            # Пояснение: если в куче лежит устаревшая запись с худшей дистанцией, пропускаем её.
            if current_distance > distances[u]:
                # Пояснение: переход к следующей итерации цикла while.
                continue
            # Пояснение: если достигли target, можно завершать поиск, потому что Дейкстра уже нашла оптимальное расстояние.
            if u == target:
                # Пояснение: прерываем цикл обработки очереди.
                break
            # Пояснение: перебираем всех соседей текущей вершины и веса рёбер до них.
            for v, weight in self.neighbors(u).items():
                # Пояснение: candidate — новая возможная дистанция до v через вершину u.
                candidate = current_distance + weight
                # Пояснение: если новый путь до v короче старого известного, обновляем информацию.
                if candidate < distances[v]:
                    # Пояснение: сохраняем новую лучшую дистанцию до вершины v.
                    distances[v] = candidate
                    # Пояснение: запоминаем, что в лучшем пути перед v стоит вершина u.
                    previous[v] = u
                    # Пояснение: добавляем вершину v в очередь с новым приоритетом candidate.
                    heapq.heappush(heap, (candidate, v))
        # Пояснение: если расстояние до target осталось бесконечным, значит target недостижим из start.
        if distances[target] == float("inf"):
            # Пояснение: для несвязного случая выбрасывается ошибка; в генераторе графов такое обычно не должно происходить.
            raise ValueError("Путь между start и target не найден.")
        # Пояснение: path будет хранить восстановленный кратчайший маршрут в обратном порядке, затем развернётся.
        path = []
        # Пояснение: начинаем восстановление маршрута с целевой вершины.
        current = target
        # Пояснение: идём по цепочке previous назад, пока не дойдём до None перед стартовой вершиной.
        while current is not None:
            # Пояснение: добавляем текущую вершину в маршрут.
            path.append(current)
            # Пояснение: переходим к предыдущей вершине оптимального пути.
            current = previous[current]
        # Пояснение: сейчас путь записан от target к start, поэтому разворачиваем его в нормальный порядок.
        path.reverse()
        # Пояснение: возвращаем пару: список вершин кратчайшего пути и его длину.
        return path, distances[target]

    # Пояснение: метод строит случайный допустимый путь от start до target.
    # Пояснение: max_steps ограничивает длину случайного блуждания, чтобы оно не выполнялось бесконечно.
    # Пояснение: rng позволяет передать генератор случайных чисел с seed для воспроизводимых экспериментов.
    def random_walk_path(self, start: int, target: int, max_steps: int | None = None, rng: random.Random | None = None) -> List[int]:
        # Пояснение: строка документации метода.
        """Построить случайный путь от start до target."""
        # Пояснение: проверяем корректность стартовой вершины.
        self._validate_vertex(start)
        # Пояснение: проверяем корректность целевой вершины.
        self._validate_vertex(target)
        # Пояснение: если внешний генератор случайных чисел не передали, создаём новый локальный random.Random().
        rng = rng or random.Random()
        # Пояснение: если max_steps не задан, берём безопасный лимит — минимум 4 шага или удвоенное число вершин.
        max_steps = max_steps or max(4, self.vertices * 2)
        # Пояснение: начинаем маршрут со стартовой вершины.
        path = [start]
        # Пояснение: visited хранит уже посещённые вершины, чтобы случайный путь не ходил по циклам.
        visited = {start}
        # Пояснение: current — текущая вершина случайного блуждания.
        current = start
        # Пояснение: выполняем не больше max_steps случайных переходов.
        for _ in range(max_steps):
            # Пояснение: если уже пришли в target, дальнейшее блуждание не нужно.
            if current == target:
                # Пояснение: выходим из цикла построения случайного пути.
                break
            # Пояснение: candidates — непосещённые соседи текущей вершины, куда можно перейти без образования цикла.
            candidates = [v for v in self.neighbors(current) if v not in visited]
            # Пояснение: если непосещённых соседей нет, случайное блуждание зашло в тупик.
            if not candidates:
                # Пояснение: выходим из цикла и позже достроим путь до target через shortest_path.
                break
            # Пояснение: исходный комментарий автора; далее кандидаты сортируются по весу ребра, чтобы чаще выбирать лёгкие переходы.
            # Смещение в сторону лёгких рёбер повышает качество начальной популяции.
            # Пояснение: сортируем соседей по возрастанию веса ребра current-v.
            candidates.sort(key=lambda v: self.edge_weight(current, v))
            # Пояснение: top_k — несколько лучших по весу кандидатов; ограничение до 4 сохраняет случайность, но не уводит слишком часто в плохие рёбра.
            top_k = candidates[: max(1, min(len(candidates), 4))]
            # Пояснение: случайно выбираем следующую вершину из top_k.
            nxt = rng.choice(top_k)
            # Пояснение: добавляем выбранную вершину в текущий путь.
            path.append(nxt)
            # Пояснение: помечаем вершину как посещённую.
            visited.add(nxt)
            # Пояснение: переносим текущую позицию случайного блуждания в выбранную вершину.
            current = nxt
        # Пояснение: если после случайной части маршрут не закончился в target, нужно достроить хвост.
        if path[-1] != target:
            # Пояснение: tail — кратчайший путь от текущей последней вершины до target; _ — длина этого пути, здесь она не нужна.
            tail, _ = self.shortest_path(path[-1], target)
            # Пояснение: добавляем хвост без первого элемента, чтобы не продублировать текущую последнюю вершину.
            path.extend(tail[1:])
        # Пояснение: перед возвратом удаляем возможные циклы и возвращаем корректный путь.
        return self.remove_cycles(path)

    # Пояснение: приватный служебный метод проверяет, находится ли номер вершины в допустимом диапазоне.
    def _validate_vertex(self, vertex: int) -> None:
        # Пояснение: вершина некорректна, если её номер меньше 0 или больше либо равен количеству вершин.
        if vertex < 0 or vertex >= self.vertices:
            # Пояснение: выбрасываем исключение, чтобы не обращаться к несуществующей вершине в списке смежности.
            raise ValueError(f"Некорректная вершина: {vertex}")


# Пояснение: функция загружает граф, стартовую и целевую вершину из JSON-файла.
# Пояснение: path может быть строкой или объектом Path.
def load_graph_from_json(path: str | Path) -> Tuple[WeightedGraph, int, int]:
    # Пояснение: строка документации функции.
    """Загрузить граф из JSON-файла."""
    # Пояснение: приводим входной путь к объекту Path для удобного чтения файла.
    source = Path(path)
    # Пояснение: читаем JSON-файл как текст UTF-8 и превращаем его в Python-словарь.
    payload = json.loads(source.read_text(encoding="utf-8"))
    # Пояснение: создаём пустой граф с количеством вершин, указанным в поле "vertices".
    graph = WeightedGraph(int(payload["vertices"]))
    # Пояснение: перебираем список рёбер из JSON; каждое ребро хранится как [u, v, weight].
    for u, v, weight in payload["edges"]:
        # Пояснение: добавляем ребро в граф, приводя номера вершин и вес к нужным типам.
        graph.add_edge(int(u), int(v), float(weight))
    # Пояснение: читаем стартовую вершину; если её нет в JSON, используем 0.
    start = int(payload.get("start", 0))
    # Пояснение: читаем целевую вершину; если её нет в JSON, используем последнюю вершину графа.
    target = int(payload.get("target", graph.vertices - 1))
    # Пояснение: возвращаем граф и две служебные вершины для задачи поиска пути.
    return graph, start, target


# Пояснение: функция сохраняет граф, стартовую и целевую вершину в JSON-файл.
def save_graph_to_json(graph: WeightedGraph, start: int, target: int, path: str | Path) -> None:
    # Пояснение: строка документации функции.
    """Сохранить граф в JSON."""
    # Пояснение: payload — словарь, который затем будет преобразован в JSON.
    payload = {
        # Пояснение: сохраняем количество вершин графа.
        "vertices": graph.vertices,
        # Пояснение: сохраняем стартовую вершину задачи.
        "start": start,
        # Пояснение: сохраняем конечную вершину задачи.
        "target": target,
        # Пояснение: сохраняем все уникальные рёбра как списки [u, v, weight].
        "edges": [[edge.u, edge.v, edge.weight] for edge in graph.edges()],
    }
    # Пояснение: записываем JSON в файл с русскоязычной поддержкой ensure_ascii=False и красивыми отступами indent=2.
    Path(path).write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")


# Пояснение: функция генерирует случайный связный взвешенный неориентированный граф.
# Пояснение: vertices — количество вершин; edges — требуемое количество рёбер.
# Пояснение: min_weight и max_weight задают диапазон случайных весов.
# Пояснение: seed позволяет воспроизвести тот же самый граф при повторном запуске.
def generate_connected_graph(vertices: int, edges: int, min_weight: int = 1, max_weight: int = 30, seed: int | None = None) -> WeightedGraph:
    # Пояснение: строка документации функции.
    """Сгенерировать связный случайный неориентированный граф."""
    # Пояснение: для связного неориентированного графа на vertices вершинах минимум нужно vertices - 1 ребро.
    if edges < vertices - 1:
        # Пояснение: если рёбер меньше минимума, связный граф построить невозможно.
        raise ValueError("Для связного графа нужно минимум vertices - 1 рёбер.")
    # Пояснение: max_edges — максимальное число рёбер в простом неориентированном графе без петель и дублей.
    max_edges = vertices * (vertices - 1) // 2
    # Пояснение: если пользователь просит больше рёбер, чем существует возможных пар вершин, это ошибка.
    if edges > max_edges:
        # Пояснение: сообщаем, что указанное количество рёбер невозможно для простого графа.
        raise ValueError("Слишком много рёбер для простого графа.")
    # Пояснение: создаём локальный генератор случайных чисел; seed делает генерацию воспроизводимой.
    rng = random.Random(seed)
    # Пояснение: создаём пустой граф с заданным количеством вершин.
    graph = WeightedGraph(vertices)
    # Пояснение: order — список номеров вершин, который будет случайно перемешан для построения связующего каркаса.
    order = list(range(vertices))
    # Пояснение: случайно перемешиваем вершины.
    rng.shuffle(order)
    # Пояснение: existing хранит уже добавленные рёбра, чтобы потом не продублировать их.
    existing = set()
    # Пояснение: первый этап генерации: создаём цепочку из vertices - 1 ребра, чтобы гарантировать связность графа.
    for i in range(vertices - 1):
        # Пояснение: берём две соседние вершины в случайном порядке order.
        u, v = order[i], order[i + 1]
        # Пояснение: добавляем ребро между ними со случайным весом в заданном диапазоне.
        graph.add_edge(u, v, rng.randint(min_weight, max_weight))
        # Пояснение: запоминаем это ребро как уже существующее.
        existing.add(tuple(sorted((u, v))))
    # Пояснение: all_pairs — все возможные пары вершин, между которыми ещё нет ребра.
    all_pairs = [(u, v) for u in range(vertices) for v in range(u + 1, vertices) if (u, v) not in existing]
    # Пояснение: перемешиваем список потенциальных дополнительных рёбер.
    rng.shuffle(all_pairs)
    # Пояснение: need — сколько дополнительных рёбер нужно добавить сверх связующего каркаса.
    need = edges - (vertices - 1)
    # Пояснение: берём первые need случайных пар из all_pairs.
    for u, v in all_pairs[:need]:
        # Пояснение: добавляем каждое дополнительное ребро со случайным положительным весом.
        graph.add_edge(u, v, rng.randint(min_weight, max_weight))
    # Пояснение: возвращаем готовый связный граф.
    return graph
