"""Сравнение результатов ИС с результатами напарника по ГА.

Сценарий нужен для финальной парной сдачи: пользователь реализует ИС,
напарник реализует ГА. Если напарник отдаст CSV в том же формате,
скрипт объединит результаты и построит сравнительные графики.

Запуск из папки "Мои решения":
    python -m src.compare_with_partner path/to/ga_results.csv

Формат CSV ожидается такой же, как у results/experiment_results.csv:
algorithm, graph_vertices, graph_edges, start_vertex, target_vertex,
best_path_length, dijkstra_path_length, deviation_percent,
execution_time_sec, iterations, best_path
"""

# Эта строка включает поведение аннотаций типов из будущих версий Python.
# Без неё Python сразу вычислял бы некоторые подсказки типов при загрузке файла.
# С ней аннотации можно безопаснее использовать для самодокументирования кода,
# особенно когда в типах встречаются современные конструкции вроде str | Path.
from __future__ import annotations

# Импортируем класс Path из стандартной библиотеки pathlib.
# Path используется для удобной и кроссплатформенной работы с путями к файлам и папкам.
# В этом файле через Path приводятся входные пути, создаются выходные пути,
# проверяется существование CSV-файлов и создаются папки results.
from pathlib import Path
# Импортируем стандартный модуль csv.
# Он нужен для чтения CSV-файлов с результатами экспериментов и записи объединённого CSV.
import csv
# Импортируем стандартный модуль sys.
# Он нужен для доступа к аргументам командной строки через sys.argv.
# Именно оттуда скрипт берёт путь к CSV-файлу напарника.
import sys
# Импортируем типы List и Dict для аннотаций.
# List[Dict[str, str]] означает список строк таблицы,
# где каждая строка представлена словарём: имя_столбца -> текстовое_значение.
from typing import List, Dict

# Импортируем модуль pyplot библиотеки matplotlib.
# Через него строятся и сохраняются сравнительные графики:
# 1) график времени выполнения;
# 2) график качества решения по отклонению от Дейкстры.
import matplotlib.pyplot as plt

# Импортируем project_root из локального модуля utils.
# Точка перед utils означает относительный импорт из текущего пакета src.
# Функция project_root должна вернуть корневую папку проекта,
# чтобы скрипт мог находить results/experiment_results.csv и сохранять новые файлы в results.
from .utils import project_root


# Функция читает CSV-файл и возвращает его содержимое в виде списка словарей.
# Каждый словарь соответствует одной строке CSV.
# Ключи словаря — названия столбцов из первой строки CSV.
# Значения словаря — текстовые значения ячеек.
# path может быть либо строкой, либо объектом Path.
def read_csv(path: str | Path) -> List[Dict[str, str]]:
    # Приводим входной путь к объекту Path.
    # Это удобно, потому что у Path есть методы exists(), open(), parent и другие.
    source = Path(path)
    # Проверяем, существует ли файл по указанному пути.
    # Если файла нет, дальнейшее чтение невозможно.
    if not source.exists():
        # Явно выбрасываем ошибку FileNotFoundError.
        # Сообщение содержит путь, чтобы было понятно, какой CSV не найден.
        raise FileNotFoundError(f"CSV-файл не найден: {source}")
    # Открываем CSV-файл на чтение.
    # 'r' означает режим чтения.
    # encoding='utf-8' нужен для корректной работы с русским текстом.
    # newline='' рекомендуется документацией csv, чтобы корректно обрабатывались переводы строк.
    with source.open('r', encoding='utf-8', newline='') as file:
        # csv.DictReader читает CSV как таблицу словарей.
        # Первая строка файла используется как список названий столбцов.
        # list(...) сразу загружает все строки файла в память.
        return list(csv.DictReader(file))


# Функция записывает список словарей в CSV-файл.
# rows — строки таблицы, которые нужно сохранить.
# path — путь, куда нужно записать CSV.
def write_csv(rows: List[Dict[str, str]], path: str | Path) -> None:
    # Приводим выходной путь к объекту Path.
    # Это позволит удобно создать родительскую папку и открыть файл на запись.
    output = Path(path)
    # Создаём родительскую папку для выходного файла.
    # parents=True означает создать всю цепочку папок, если её нет.
    # exist_ok=True означает не считать ошибкой ситуацию, когда папка уже существует.
    output.parent.mkdir(parents=True, exist_ok=True)
    # Проверяем, есть ли вообще строки для записи.
    # Без строк нельзя определить набор столбцов для DictWriter.
    if not rows:
        # Если список строк пустой, выбрасываем понятную ошибку.
        raise ValueError('Нет строк для записи.')
    # Открываем выходной CSV-файл на запись.
    # 'w' означает режим записи с перезаписью существующего файла.
    # encoding='utf-8' сохраняет русские заголовки и значения без проблем.
    # newline='' нужен для корректной записи CSV без лишних пустых строк.
    with output.open('w', encoding='utf-8', newline='') as file:
        # Создаём объект writer, который умеет записывать словари в CSV.
        # fieldnames берём из ключей первой строки.
        # Это означает, что порядок столбцов будет таким, как в первом словаре rows[0].
        writer = csv.DictWriter(file, fieldnames=list(rows[0].keys()))
        # Записываем строку заголовков CSV.
        writer.writeheader()
        # Записываем все строки данных.
        # Каждый словарь из rows станет отдельной строкой CSV.
        writer.writerows(rows)


# Функция строит линейный график по выбранной метрике и сохраняет его в PNG.
# rows — объединённые строки результатов ИС и ГА.
# metric — название столбца CSV, который нужно отложить по оси Y.
# ylabel — подпись оси Y на русском языке.
# title — заголовок графика.
# output_path — путь, куда сохранить PNG-файл.
def plot_metric(rows: List[Dict[str, str]], metric: str, ylabel: str, title: str, output_path: str | Path) -> None:
    # Приводим путь сохранения графика к объекту Path.
    output = Path(output_path)
    # Создаём папку, в которую будет сохранён график.
    # Если папка results уже есть, ошибка не возникнет.
    output.parent.mkdir(parents=True, exist_ok=True)

    # Создаём пустой список алгоритмов.
    # В него будут добавлены уникальные названия алгоритмов из CSV.
    # Например: "Immune Network" и "Genetic Algorithm".
    algorithms = []
    # Проходим по всем строкам объединённой таблицы.
    for row in rows:
        # Достаём название алгоритма из столбца 'algorithm'.
        # Важно: такой столбец обязательно должен быть в CSV.
        alg = row['algorithm']
        # Проверяем, добавляли ли мы уже такой алгоритм в список.
        # Это сохраняет уникальность списка algorithms.
        if alg not in algorithms:
            # Добавляем новый алгоритм в список.
            # Порядок будет соответствовать первому появлению алгоритма в CSV.
            algorithms.append(alg)

    # Создаём новую фигуру matplotlib размером 10 на 6 дюймов.
    # Это холст, на котором будет построен график.
    plt.figure(figsize=(10, 6))
    # Для каждого алгоритма строим отдельную линию на одном общем графике.
    for algorithm in algorithms:
        # Отбираем только те строки, которые относятся к текущему алгоритму.
        # Например, отдельно строки ИС и отдельно строки ГА.
        subset = [r for r in rows if r['algorithm'] == algorithm]
        # Сортируем строки по количеству вершин графа.
        # Это нужно, чтобы линия шла слева направо по росту размерности задачи.
        # int(float(...)) используется на случай, если число в CSV записано как "20.0".
        subset.sort(key=lambda r: int(float(r['graph_vertices'])))
        # Формируем значения оси X.
        # По X откладывается количество вершин в графе.
        x = [int(float(r['graph_vertices'])) for r in subset]
        # Формируем значения оси Y.
        # По Y откладывается выбранная метрика: время или отклонение.
        y = [float(r[metric]) for r in subset]
        # Строим линию для текущего алгоритма.
        # marker='o' добавляет точки на измерениях.
        # linewidth=2 делает линию достаточно заметной.
        # label=algorithm нужен для легенды.
        plt.plot(x, y, marker='o', linewidth=2, label=algorithm)

    # Подписываем ось X.
    plt.xlabel('Количество вершин')
    # Подписываем ось Y текстом, переданным в параметре ylabel.
    plt.ylabel(ylabel)
    # Добавляем заголовок графика.
    plt.title(title)
    # Включаем сетку, чтобы было легче читать значения.
    # alpha=0.35 делает сетку полупрозрачной.
    plt.grid(True, alpha=0.35)
    # Показываем легенду с названиями алгоритмов.
    plt.legend()
    # Автоматически подгоняем отступы, чтобы подписи не обрезались.
    plt.tight_layout()
    # Сохраняем график в PNG-файл.
    # dpi=180 задаёт достаточно хорошее качество изображения для отчёта.
    plt.savefig(output, dpi=180)
    # Закрываем фигуру, чтобы не держать её в памяти.
    # Это важно, если таких графиков строится несколько подряд.
    plt.close()


# Главная функция сценария.
# Она запускается, когда файл выполняют как модуль или как скрипт.
# В ней происходит чтение результатов ИС, чтение результатов ГА,
# объединение таблиц и построение сравнительных графиков.
def main() -> None:
    # Получаем корневую папку проекта.
    # От неё дальше строятся пути к папке results.
    root = project_root()
    # Формируем путь к CSV с собственными результатами ИС.
    # Этот файл должен быть заранее создан экспериментами твоего проекта.
    own_csv = root / 'results' / 'experiment_results.csv'

    # Проверяем, передал ли пользователь путь к CSV напарника через командную строку.
    # sys.argv[0] — имя запускаемого модуля/скрипта.
    # sys.argv[1] должен быть путём к CSV напарника.
    # Если длина меньше 2, значит путь не был передан.
    if len(sys.argv) < 2:
        # Печатаем подсказку, что нужно указать CSV с результатами генетического алгоритма.
        print('Укажите путь к CSV напарника с результатами ГА.')
        # Печатаем пример правильного запуска из командной строки.
        print('Пример: python -m src.compare_with_partner ../ga_results.csv')
        # Печатаем путь, где уже должен лежать CSV с результатами твоей ИС.
        # Это помогает проверить, что собственные эксперименты уже были запущены.
        print(f'CSV с результатами ИС уже лежит здесь: {own_csv}')
        # Завершаем функцию без ошибки.
        # Скрипт ничего не строит, потому что CSV напарника не был указан.
        return

    # Берём путь к CSV напарника из первого аргумента командной строки.
    # Например, если команда была python -m src.compare_with_partner ../ga_results.csv,
    # то sys.argv[1] будет равен ../ga_results.csv.
    partner_csv = Path(sys.argv[1])
    # Читаем собственный CSV с результатами искусственной иммунной сети.
    # На выходе получаем список словарей.
    own_rows = read_csv(own_csv)
    # Читаем CSV напарника с результатами генетического алгоритма.
    # Формат должен совпадать с форматом experiment_results.csv.
    partner_rows = read_csv(partner_csv)
    # Объединяем две таблицы простым сложением списков.
    # Сначала идут строки ИС, затем строки ГА.
    combined = own_rows + partner_rows

    # Формируем путь к итоговому объединённому CSV.
    # Он будет сохранён в папке results проекта.
    output_csv = root / 'results' / 'combined_results_ga_vs_is.csv'
    # Записываем объединённые строки в CSV.
    write_csv(combined, output_csv)

    # Строим первый сравнительный график.
    # Он сравнивает время выполнения ИС и ГА при разных размерах графа.
    plot_metric(
        # Передаём объединённые строки ИС и ГА.
        combined,
        # Указываем столбец CSV, который будет использован как значение Y.
        metric='execution_time_sec',
        # Подписываем ось Y.
        ylabel='Время выполнения, сек',
        # Задаём заголовок графика.
        title='Сравнение времени выполнения: ГА и ИС',
        # Указываем путь сохранения PNG-файла.
        output_path=root / 'results' / 'comparison_time_ga_vs_is.png',
    )

    # Строим второй сравнительный график.
    # Он сравнивает качество решений ИС и ГА через отклонение от эталона Дейкстры.
    plot_metric(
        # Передаём те же объединённые строки.
        combined,
        # По Y теперь используется столбец deviation_percent.
        # Чем меньше это значение, тем ближе алгоритм к эталонному кратчайшему пути.
        metric='deviation_percent',
        # Подписываем ось Y.
        ylabel='Отклонение от эталона Дейкстры, %',
        # Задаём заголовок графика качества.
        title='Сравнение качества решений: ГА и ИС',
        # Указываем путь сохранения второго PNG-файла.
        output_path=root / 'results' / 'comparison_quality_ga_vs_is.png',
    )

    # Сообщаем пользователю в консоль, куда сохранена объединённая таблица.
    print(f'Объединённая таблица сохранена: {output_csv}')
    # Сообщаем, что сравнительные графики также успешно сохранены.
    print('Графики сохранены в папку results/.')


# Стандартная Python-проверка точки входа.
# Этот блок выполнится только если файл запущен напрямую как исполняемый модуль.
# Если файл просто импортируют из другого файла, main() автоматически не запустится.
if __name__ == '__main__':
    # Запускаем основную функцию сценария.
    main()
