# Пояснение: строка ниже является строкой документации модуля; файл содержит функции визуализации графа и экспериментов.
"""Визуализация графа, найденного пути и экспериментальных графиков."""

# Пояснение: импорт из __future__ откладывает вычисление аннотаций типов и делает типизацию совместимее.
from __future__ import annotations

# Пояснение: Path используется для работы с путями к выходным PNG-файлам.
from pathlib import Path
# Пояснение: List и Sequence используются в аннотациях типов для списков и последовательностей.
from typing import List, Sequence

# Пояснение: matplotlib.pyplot используется для построения и сохранения графиков.
import matplotlib.pyplot as plt

# Пояснение: импортируем модель взвешенного графа, чтобы функции могли принимать объект WeightedGraph.
from .graph_model import WeightedGraph


# Пояснение: функция рисует граф, подписывает веса рёбер и подсвечивает найденный путь.
# Пояснение: graph — объект взвешенного графа; path — найденный маршрут; start и target — начальная и конечная вершины.
# Пояснение: output_path — путь к PNG-файлу; title — необязательный заголовок изображения.
def draw_graph_with_path(graph: WeightedGraph, path: List[int], start: int, target: int, output_path: str | Path, title: str | None = None) -> None:
    # Пояснение: строка документации функции.
    """Сохранить изображение графа с подсвеченным найденным путём."""
    # Пояснение: networkx импортируется внутри функции, потому что он нужен только для визуализации графа.
    import networkx as nx
    # Пояснение: приводим путь к выходному файлу к объекту Path.
    output = Path(output_path)
    # Пояснение: создаём родительскую папку для PNG-файла, если она отсутствует.
    output.parent.mkdir(parents=True, exist_ok=True)
    # Пояснение: создаём объект графа NetworkX, чтобы использовать готовые функции отрисовки.
    nx_graph = nx.Graph()
    # Пояснение: добавляем в NetworkX-граф все вершины от 0 до graph.vertices - 1.
    for vertex in range(graph.vertices):
        # Пояснение: добавляем одну вершину в граф для визуализации.
        nx_graph.add_node(vertex)
    # Пояснение: перебираем все уникальные рёбра из модели WeightedGraph.
    for edge in graph.edges():
        # Пояснение: добавляем ребро в NetworkX-граф и сохраняем его вес как атрибут weight.
        nx_graph.add_edge(edge.u, edge.v, weight=edge.weight)
    # Пояснение: рассчитываем координаты вершин через spring_layout; вес ребра влияет на раскладку.
    pos = nx.spring_layout(nx_graph, seed=7, weight='weight')
    # Пояснение: создаём фигуру matplotlib размером 12 на 8 дюймов.
    plt.figure(figsize=(12, 8))
    # Пояснение: рисуем все рёбра графа тонкими полупрозрачными линиями.
    nx.draw_networkx_edges(nx_graph, pos, width=1.2, alpha=0.35)
    # Пояснение: рисуем все вершины базовым размером.
    nx.draw_networkx_nodes(nx_graph, pos, node_size=650)
    # Пояснение: подписываем номера вершин.
    nx.draw_networkx_labels(nx_graph, pos, font_size=10, font_weight='bold')
    # Пояснение: формируем словарь подписей рёбер: ключ — пара вершин, значение — вес ребра.
    # Пояснение: если вес целый, он приводится к int, чтобы на графике не было лишнего .0.
    labels = {(edge.u, edge.v): int(edge.weight) if float(edge.weight).is_integer() else edge.weight for edge in graph.edges()}
    # Пояснение: рисуем подписи весов рёбер на изображении графа.
    nx.draw_networkx_edge_labels(nx_graph, pos, edge_labels=labels, font_size=8)
    # Пояснение: если найденный путь существует и содержит хотя бы два узла, подсвечиваем его.
    if path and len(path) >= 2:
        # Пояснение: path_edges — список рёбер, входящих в найденный путь.
        # Пояснение: sorted нужен, потому что граф неориентированный и ребро (u, v) равно ребру (v, u).
        path_edges = [tuple(sorted((u, v))) for u, v in zip(path, path[1:])]
        # Пояснение: перерисовываем рёбра найденного пути более толстой линией.
        nx.draw_networkx_edges(nx_graph, pos, edgelist=path_edges, width=4.0)
        # Пояснение: перерисовываем вершины найденного пути увеличенным размером.
        nx.draw_networkx_nodes(nx_graph, pos, nodelist=path, node_size=760)
    # Пояснение: отдельно выделяем стартовую вершину ещё более крупным размером.
    nx.draw_networkx_nodes(nx_graph, pos, nodelist=[start], node_size=900)
    # Пояснение: отдельно выделяем целевую вершину ещё более крупным размером.
    nx.draw_networkx_nodes(nx_graph, pos, nodelist=[target], node_size=900)
    # Пояснение: задаём заголовок изображения; если title не передан, используется стандартный заголовок.
    plt.title(title or 'Искусственная иммунная сеть: найденный путь')
    # Пояснение: отключаем оси координат, потому что для графа они не несут смысла.
    plt.axis('off')
    # Пояснение: автоматически подгоняем отступы, чтобы элементы не обрезались.
    plt.tight_layout()
    # Пояснение: сохраняем изображение в файл с разрешением 180 dpi.
    plt.savefig(output, dpi=180)
    # Пояснение: закрываем фигуру, чтобы не накапливать открытые графики в памяти.
    plt.close()


# Пояснение: функция строит график зависимости времени выполнения от размерности графа.
# Пояснение: rows — строки результатов экспериментов; output_path — путь к PNG-файлу.
def plot_time_vs_size(rows: Sequence[dict], output_path: str | Path) -> None:
    # Пояснение: строка документации функции.
    """Построить график зависимости времени от размерности графа."""
    # Пояснение: приводим путь к выходному изображению к объекту Path.
    output = Path(output_path)
    # Пояснение: создаём родительскую папку для графика, если она отсутствует.
    output.parent.mkdir(parents=True, exist_ok=True)
    # Пояснение: извлекаем из каждой строки количество вершин и приводим его к int.
    vertices = [int(row['graph_vertices']) for row in rows]
    # Пояснение: извлекаем из каждой строки время выполнения и приводим его к float.
    times = [float(row['execution_time_sec']) for row in rows]
    # Пояснение: создаём новую фигуру matplotlib размером 10 на 6 дюймов.
    plt.figure(figsize=(10, 6))
    # Пояснение: строим линейный график времени от количества вершин с маркерами точек.
    plt.plot(vertices, times, marker='o', linewidth=2)
    # Пояснение: подпись оси X — размерность графа по числу вершин.
    plt.xlabel('Количество вершин')
    # Пояснение: подпись оси Y — среднее время выполнения алгоритма.
    plt.ylabel('Среднее время выполнения, сек')
    # Пояснение: заголовок графика.
    plt.title('Зависимость времени выполнения ИС от размерности графа')
    # Пояснение: включаем сетку с небольшой прозрачностью, чтобы график было проще читать.
    plt.grid(True, alpha=0.35)
    # Пояснение: подгоняем отступы перед сохранением.
    plt.tight_layout()
    # Пояснение: сохраняем график в PNG-файл.
    plt.savefig(output, dpi=180)
    # Пояснение: закрываем фигуру и освобождаем ресурсы matplotlib.
    plt.close()


# Пояснение: функция строит график изменения целевой функции по итерациям иммунного алгоритма.
# Пояснение: history — последовательность лучших значений длины пути по итерациям.
# Пояснение: optimal — необязательное эталонное значение Дейкстры для горизонтальной линии.
def plot_objective_history(history: Sequence[float], output_path: str | Path, optimal: float | None = None) -> None:
    # Пояснение: строка документации функции.
    """Построить график значения целевой функции по итерациям."""
    # Пояснение: приводим путь к выходному изображению к объекту Path.
    output = Path(output_path)
    # Пояснение: создаём родительскую папку для изображения, если она отсутствует.
    output.parent.mkdir(parents=True, exist_ok=True)
    # Пояснение: создаём новую фигуру matplotlib размером 10 на 6 дюймов.
    plt.figure(figsize=(10, 6))
    # Пояснение: строим линию лучшего значения ИС по итерациям; итерации нумеруются с 1.
    plt.plot(list(range(1, len(history) + 1)), history, linewidth=2, label='Лучшее значение ИС')
    # Пояснение: если задан эталон, добавляем его на график как горизонтальную пунктирную линию.
    if optimal is not None:
        # Пояснение: рисуем горизонтальную линию уровня optimal, то есть длины кратчайшего пути по Дейкстре.
        plt.axhline(optimal, linestyle='--', linewidth=1.5, label='Эталон Дейкстры')
    # Пояснение: подпись оси X — номер итерации алгоритма.
    plt.xlabel('Итерация')
    # Пояснение: подпись оси Y — длина найденного пути, то есть значение целевой функции.
    plt.ylabel('Длина найденного пути')
    # Пояснение: заголовок графика.
    plt.title('Зависимость значения целевой функции от числа итераций')
    # Пояснение: включаем сетку для читаемости графика.
    plt.grid(True, alpha=0.35)
    # Пояснение: выводим легенду, чтобы отличать линию ИС от эталона Дейкстры.
    plt.legend()
    # Пояснение: автоматически подгоняем отступы.
    plt.tight_layout()
    # Пояснение: сохраняем график в PNG-файл.
    plt.savefig(output, dpi=180)
    # Пояснение: закрываем фигуру и освобождаем память.
    plt.close()
