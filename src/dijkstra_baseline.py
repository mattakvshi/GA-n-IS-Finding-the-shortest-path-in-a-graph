"""Эталонное решение задачи кратчайшего пути методом Дейкстры."""

# Пояснение: импорт из __future__ откладывает вычисление аннотаций типов; это упрощает работу с современными типами и повышает совместимость.
from __future__ import annotations

# Пояснение: dataclass автоматически создаёт для класса служебные методы, например __init__ и __repr__.
from dataclasses import dataclass
# Пояснение: List используется для аннотации списка вершин в найденном пути.
from typing import List

# Пояснение: импортируется модель взвешенного графа, в которой уже реализован метод shortest_path.
from .graph_model import WeightedGraph


# Пояснение: dataclass делает класс DijkstraResult компактной структурой данных.
# Пояснение: frozen=True запрещает менять поля результата после создания объекта; это удобно для эталонного результата.
@dataclass(frozen=True)
# Пояснение: класс DijkstraResult хранит результат работы алгоритма Дейкстры.
class DijkstraResult:
    # Пояснение: path — список номеров вершин, через которые проходит кратчайший путь.
    path: List[int]
    # Пояснение: length — суммарная длина этого пути, то есть сумма весов всех рёбер маршрута.
    length: float


# Пояснение: функция-обёртка запускает эталонный алгоритм Дейкстры для заданного графа и пары вершин.
# Пояснение: graph — взвешенный граф; start — начальная вершина; target — конечная вершина.
# Пояснение: возвращается объект DijkstraResult с путём и его длиной.
def solve_with_dijkstra(graph: WeightedGraph, start: int, target: int) -> DijkstraResult:
    # Пояснение: строка документации функции; подчёркивает, что результат используется как эталон.
    """Вернуть эталонный кратчайший путь и его длину."""
    # Пояснение: вызываем метод shortest_path у графа; внутри него реализован алгоритм Дейкстры.
    path, length = graph.shortest_path(start, target)
    # Пояснение: упаковываем найденный путь и длину в dataclass-объект, чтобы дальше обращаться к полям как result.path и result.length.
    return DijkstraResult(path=path, length=length)
